import Movie from '../domain/Movie';

test('Объект должен создаваться', () => {
    const unit = new Movie(2012, 'США', 'Avengers Assemble!', ['фантастика', 'боевик', 'фентези', 'приключения'], '137 мин. / 02:17');
    expect(unit).toEqual({год: 2012, страна: 'США', слоган: 'Avengers Assemble!', жанр: ['фантастика', 'боевик', 'фентези', 'приключения'], время: '137 мин. / 02:17'})
});
